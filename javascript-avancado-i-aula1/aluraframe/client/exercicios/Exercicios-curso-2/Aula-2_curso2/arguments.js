function exibeNomeCompleto() {

    alert(`${arguments[0]} ${arguments[1]}`);
  }
  
  exibeNomeCompleto('Flávio', 'Almeida');


  /*
  Podemos ter acesso aos parâmetros passados com 'arguments'
  que é uma var implícita que nos dá acesso a todos
  os parâmetros passados para a função ou método. 
  */